// Regenerates the MILB_PARENT_ORG block in src/lib/teams.js — every current
// MiLB affiliate's teamId mapped to its parent MLB org's teamId, so a
// farmhand's headshot can tint with his org's TEAM_COLORS entry the same way
// an MLB player's does (see teamTintColor). Reads public/data/affiliates.json
// (the same file src/api/team.js's fetchAffiliateTree reads) rather than
// hitting statsapi directly, so this only needs `npm run` — no network call —
// as long as that file is reasonably fresh.
//
// An org's farm system changes at most once a year (the PDC realignment,
// each offseason) and essentially never mid-season, so — like
// gen-milb-history.mjs — this is run BY HAND after a realignment, not on a
// cron: node scripts/gen-milb-team-colors.mjs
import { readFile, writeFile } from 'node:fs/promises'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const affiliatesPath = join(here, '..', 'public', 'data', 'affiliates.json')
const teamsPath = join(here, '..', 'src', 'lib', 'teams.js')

const BEGIN = '// MILB_PARENT_ORG:BEGIN (generated by scripts/gen-milb-team-colors.mjs)'
const END = '// MILB_PARENT_ORG:END'

async function main() {
  const { byOrgId } = JSON.parse(await readFile(affiliatesPath, 'utf8'))
  const orgIds = Object.keys(byOrgId).sort((a, b) => Number(a) - Number(b))

  const lines = []
  for (const orgId of orgIds) {
    for (const aff of byOrgId[orgId]) {
      lines.push(`  ${aff.id}: ${orgId}, // ${aff.name}`)
    }
  }

  const block = `${BEGIN}\nconst MILB_PARENT_ORG = {\n${lines.join('\n')}\n}\n${END}`

  const escape = (s) => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
  const teamsSrc = await readFile(teamsPath, 'utf8')
  const pattern = new RegExp(`${escape(BEGIN)}[\\s\\S]*?${escape(END)}`)
  if (!pattern.test(teamsSrc)) {
    throw new Error(`Could not find ${BEGIN} … ${END} markers in ${teamsPath}`)
  }
  await writeFile(teamsPath, teamsSrc.replace(pattern, block))

  console.log(`Wrote ${lines.length} MiLB affiliates across ${orgIds.length} orgs into ${teamsPath}`)
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
